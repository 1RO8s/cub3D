#include <stddef.h>

void	*ft_memset(void *buf, int ch, size_t n)
{
	size_t	i;

	i = 0;
	while (n > 0)
	{
		*((char *)buf + i) = ch;
		n--;
		i++;
	}
	return (buf);
}
